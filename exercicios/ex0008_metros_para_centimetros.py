# leia um programa que leia um valor em metros
# e exiba em centimetros e milimetros.
metros = float(input("Digite a medida em M: "))
centimetros = metros * 100
milimetros = metros * 1000
print("Essa menida em centimetros vale {:.0f}cm \nEssa medida em milimetros vale {:.0f}mm".format(centimetros,milimetros))

